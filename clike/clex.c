ksm_t ksm_clike_lex_unicode(void)
{
  int ch = 0, c;
  char *p, *q;
  
  p = yytext + 4;
  q = yytext + yyleng - 1;
  while( p < q ){
    c = *p++;
    ch *= 16;
    if( c >= '0' && c <= '9' )
      ch += c - '0';
    else if( c >= 'A' && c <= 'F' )
      ch += c - 'A' + 10;
    else if( c >= 'a' && c <= 'f' )
      ch += c - 'a' + 10;
    else
      ksm_error("can't happen in ksm_clike_lex_unicode");
  }
  return ksm_make_char(ch);
}

