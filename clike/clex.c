static int sbuf_size;
static char *sbuf;

ksm_t ksm_clike_complex_string(void)
{
  int n, ch, nn;
  char *s, *d;

  n = yyleng - 2;
  if( sbuf_size < n ){
    if( sbuf )
      ksm_dispose(sbuf);
    sbuf_size = n;
    sbuf = ksm_alloc(sbuf_size);
  }
  s = yytext+1;
  d = sbuf;
  while( n-- > 0 ){
    ch = *s++;
    if( ch == '"' )
      break;
    if( ch == '\\' ){
      if( n <= 0 )
	ksm_error("can't happen in ksm_clike_complex_string");
      --n;
      ch = *s++;
      switch(ch){
      case '"': break;
      case 'n': ch = '\n'; break;
      case 'r': ch = '\r'; break;
      case 't': ch = '\t'; break;
      case '\\': ch = '\\'; break;
      case 'U':
	{
	  int trail, i, n;
	  char buf[3];

	  if( n <= 0 )
	    ksm_error("unexpected EOF while reading unicode");
	  ch = *s++;
	  --n;
	  if( ch != '{' )
	    ksm_error("can't read unicode: \\U%c\n", ch);
	  ch = 0;
	  while( 1 ){
	    if( n <= 0 )
	      ksm_error("unexpected EOF while reading unicode");
	    trail = *s++;
	    --n;
	    if( trail >= '0' && trail <= '9' ){
	      ch <<= 4;
	      ch += (trail - '0');
	    }
	    else if( trail >= 'a' && trail <= 'f' ){
	      ch <<= 4;
	      ch += (trail + 10 - 'a');
	    }
	    else if( trail >= 'A' && trail <= 'F' ){
	      ch <<= 4; 
	      ch += (trail + 10 - 'A');
	    }
	    else
	      break;
	  }
	  if( trail != '}' )
	    ksm_error("unexpected character while reading unicode: 0X%X", 
		      trail);
	  nn = ksm_utf8_from_ucs4(&buf[0], ch);
	  for(i=0;i<nn;++i)
	    *d++ = buf[i];
	  continue;
	}
      default: 
	*d++ = '\\';
	break;
      }
      *d++ = ch;
    }
    else
      *d++ = ch;
  }
  return ksm_make_string_n(sbuf, d-sbuf);
}

